import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const version = "2026-05-14.beta-direct.v1";
const documents = [
  { id: "installer-terms", path: "docs/legal/INSTALLER_TERMS.md" },
  { id: "eula", path: "docs/legal/EULA.md" },
  { id: "privacy", path: "docs/legal/PRIVACY.md" },
  { id: "refund-policy", path: "docs/legal/REFUND_POLICY.md" },
  { id: "digital-content-waiver", path: "docs/legal/DIGITAL_CONTENT_WAIVER.md" },
  { id: "ai-agent-risk-notice", path: "docs/legal/AI_AGENT_RISK_NOTICE.md" },
  { id: "openclaw-mit-notice", path: "docs/legal/OPENCLAW_MIT_NOTICE.md" },
  { id: "openclaw-upstream-license", path: "docs/legal/OPENCLAW_UPSTREAM_LICENSE.md" },
];
const outputPath = path.join(root, "src", "lib", "legalConsentManifest.ts");
const checkOnly = process.argv.includes("--check");

async function fileDigest(relativePath) {
  const content = await readFile(path.join(root, relativePath));
  return {
    sha256: createHash("sha256").update(content).digest("hex"),
    bytes: content.byteLength,
  };
}

const entries = [];
for (const document of documents) {
  entries.push({
    ...document,
    ...(await fileDigest(document.path)),
  });
}

const aggregateHash = createHash("sha256")
  .update(version)
  .update("\n")
  .update(entries.map((entry) => `${entry.id}:${entry.path}:${entry.sha256}:${entry.bytes}`).join("\n"))
  .digest("hex");

const generated = `// This file is generated by scripts/generate-legal-consent.mjs. Do not edit manually.
export const legalConsentVersion = ${JSON.stringify(version)};
export const legalConsentDocumentHash = ${JSON.stringify(`sha256-${aggregateHash}`)};
export const legalConsentDocuments = ${JSON.stringify(entries, null, 2)} as const;
`;

if (checkOnly) {
  let existing = "";
  try {
    existing = await readFile(outputPath, "utf8");
  } catch {
    // handled below
  }
  if (existing !== generated) {
    throw new Error("legalConsentManifest.ts is stale. Run npm run legal:manifest.");
  }
} else {
  await writeFile(outputPath, generated);
  console.log(`generated ${path.relative(root, outputPath)}`);
}
