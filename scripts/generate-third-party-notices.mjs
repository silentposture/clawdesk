import fs from "node:fs/promises";
import path from "node:path";

const cwd = process.cwd();
const outputPath = path.join(cwd, "docs", "legal", "THIRD_PARTY_NOTICES.md");
const checkOnly = process.argv.includes("--check");

async function readJson(relativePath) {
  return JSON.parse(await fs.readFile(path.join(cwd, relativePath), "utf8"));
}

const packageJson = await readJson("package.json");
const cargoToml = await fs.readFile(path.join(cwd, "src-tauri", "Cargo.toml"), "utf8");

const npmDeps = Object.keys(packageJson.dependencies ?? {}).sort();
const rustDeps = [];
let inDeps = false;
for (const line of cargoToml.split(/\r?\n/)) {
  if (/^\[dependencies\]/.test(line)) {
    inDeps = true;
    continue;
  }
  if (/^\[/.test(line)) inDeps = false;
  if (!inDeps || !line.includes("=")) continue;
  rustDeps.push(line.split("=")[0].trim());
}
rustDeps.sort();

const generated = `# ClawDesk Third-Party Notices

Version: ${packageJson.version}
Last updated: 2026-05-14

This file is generated by \`scripts/generate-third-party-notices.mjs\`.

## Direct npm dependencies

${npmDeps.map((name) => `- \`${name}\``).join("\n")}

## Direct Rust dependencies

${rustDeps.map((name) => `- \`${name}\``).join("\n")}

## OpenClaw compatibility notice

ClawDesk imports the OpenClaw provider/auth contract for Windows Beta compatibility.

- Upstream repository: https://github.com/openclaw/openclaw
- Imported commit: \`d4484158d9291820d7af236d4277704da019f609\`
- License: MIT, copied in \`docs/legal/OPENCLAW_UPSTREAM_LICENSE.md\`

If a future release includes, modifies, compiles, or redistributes additional OpenClaw upstream source code, the release must keep the upstream copyright notice, MIT permission notice, LICENSE, and NOTICE files.

## Production requirement

Before a commercial release, generate full npm and Rust SBOM artifacts and review license deltas against the legal allowlist.
`;

if (checkOnly) {
  const existing = await fs.readFile(outputPath, "utf8").catch(() => "");
  if (existing !== generated) throw new Error("THIRD_PARTY_NOTICES.md is stale. Run npm run legal:notices.");
} else {
  await fs.writeFile(outputPath, generated, "utf8");
  console.log(`generated ${path.relative(cwd, outputPath)}`);
}
