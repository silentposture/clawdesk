const SUPPORTED_PREFERENCES: LocalePreference[] = ["system", "zh-TW", "en-US", "ja-JP"];
const LOCALE_FROM_BROWSER: Array<[string, LocaleCode]> = [
  ["zh", "zh-TW"],
  ["ja", "ja-JP"],
  ["en", "en-US"],
];

function readPreferredLocaleFromStorage(): LocalePreference {
  if (typeof localStorage === "undefined") {
    return DEFAULT_PREFERENCE;
  }
  const raw = localStorage.getItem(STORAGE_KEY);
  return normalizeLocalePreference(raw);
}

export function normalizeLocalePreference(value: string | null): LocalePreference {
  if (!value) return DEFAULT_PREFERENCE;
  if (SUPPORTED_PREFERENCES.includes(value as LocalePreference)) {
    return value as LocalePreference;
  }
  return DEFAULT_PREFERENCE;
}

export function resolveSystemLocaleFromLanguages(languages: readonly string[]): LocaleCode {
  for (const language of languages) {
    const normalized = language.trim().toLowerCase();
    if (!normalized) continue;
    for (const [prefix, locale] of LOCALE_FROM_BROWSER) {
      if (normalized.startsWith(prefix)) return locale;
    }
  }
  return "en-US";
}

export function detectSystemLocale(): LocaleCode {
  if (typeof navigator === "undefined") return "en-US";
  const languages = Array.isArray(navigator.languages) && navigator.languages.length > 0 ? navigator.languages : [navigator.language];
  return resolveSystemLocaleFromLanguages(languages);
}

export function fallbackResolveLocale(preference: LocalePreference): LocaleCode {
  if (preference === "system") return detectSystemLocale();
  return preference;
}

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? `{${token}}`));
}

export function createFormatter(locale: LocaleCode) {
  return function t(key: string, params?: Record<string, string | number>): string {
    const map = locales[locale];
    const template = map[key] ?? locales[DEFAULT_LOCALE][key] ?? key;
    return interpolate(template, params);
  };
}

export function buildSupportedLocales(locale: LocaleCode): Array<{ value: LocalePreference; label: string }> {
  const t = createFormatter(locale);
  return SUPPORTED_PREFERENCES.map((preference) => {
    if (preference === "system") {
      return { value: "system", label: t("app.locale.system") };
    }
    const key = preference === "zh-TW" ? "app.locale.zhTW" : preference === "en-US" ? "app.locale.enUS" : "app.locale.jaJP";
    return { value: preference, label: t(key) };
  });
}

export const I18nContext = createContext<I18nContextState>({
  locale: DEFAULT_LOCALE,
  preference: DEFAULT_PREFERENCE,
  t: createFormatter(DEFAULT_LOCALE),
  setLocale: () => undefined,
  supportedLocales: buildSupportedLocales(DEFAULT_LOCALE),
});

export function I18nProvider({ children }: { children: ReactNode }): JSX.Element {
  const [preference, setPreferenceState] = useState<LocalePreference>(() => {
    if (typeof document === "undefined") return DEFAULT_PREFERENCE;
    return readPreferredLocaleFromStorage();
  });
  const [locale, setLocale] = useState<LocaleCode>(() => fallbackResolveLocale(preference));
  const supportedLocales = useMemo(() => buildSupportedLocales(locale), [locale]);
  const t = useMemo(() => createFormatter(locale), [locale]);

  useEffect(() => {
    if (typeof localStorage === "undefined") return;
    const effective = readPreferredLocaleFromStorage();
    setPreferenceState(effective);
    setLocale(fallbackResolveLocale(effective));
  }, []);

  useEffect(() => {
    if (typeof localStorage !== "undefined") {
      localStorage.setItem(STORAGE_KEY, preference);
    }
    const nextLocale = fallbackResolveLocale(preference);
    setLocale(nextLocale);
  }, [preference]);

  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = locale;
    document.documentElement.dir = "ltr";
  }, [locale]);

  useEffect(() => {
    if (preference !== "system" || typeof window === "undefined") return;
    const handleLanguageChange = () => setLocale(detectSystemLocale());
    window.addEventListener("languagechange", handleLanguageChange);
    return () => window.removeEventListener("languagechange", handleLanguageChange);
  }, [preference]);

  const context = useMemo(
    () => ({
      locale,
      preference,
      t,
      setLocale: (next: LocalePreference) => setPreferenceState(next),
      supportedLocales,
    }),
    [locale, preference, t, supportedLocales],
  );

  return <I18nContext.Provider value={context}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextState {
  return useContext(I18nContext);
}
